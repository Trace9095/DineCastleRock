/** Group sources by category for admin / brand page display. */
export function getSourcesByCategory(): Record<string, ReferralSource[]> {
  const grouped: Record<string, ReferralSource[]> = {}
  for (const source of Object.values(referralSources)) {
    if (!grouped[source.category]) {
      grouped[source.category] = []
    }
    grouped[source.category].push(source)
  }
  return grouped
}
